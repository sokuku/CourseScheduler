    private void dropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseButtonActionPerformed
        String courseCode = (String) dropCourseComboBox.getSelectedItem();
        String action = String.format("Course %s has been removed.\n", courseCode);
        dropCourseTextArea.append(action);
        removeFromCourseLists(courseCode);
        
        //message for scheduled and waitlisted students being removed
        for (ScheduleEntry scheduledStudent : ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode)){
            String studentID = scheduledStudent.getStudentID();
            String studentName = getStudentName(studentID);
            
            action = String.format("%s has been dropped from %s.\n", studentName, courseCode);
            dropCourseTextArea.append(action);
        }     
        for (ScheduleEntry waitlistStudent : ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode)){
            String studentID = waitlistStudent.getStudentID();
            String studentName = getStudentName(studentID);
            
            action = String.format("%s has been dropped from %s.\n", studentName, courseCode);
            dropCourseTextArea.append(action);
        }   
        
        ScheduleQueries.dropScheduleByCourse(currentSemester, courseCode);
        CourseQueries.dropCourse(currentSemester, courseCode);
    }//GEN-LAST:event_dropCourseButtonActionPerformed

