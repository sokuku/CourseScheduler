    private void scheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseButtonActionPerformed
        String studentID = currentStudent.getStudentID();
        String studentName = getStudentName(studentID);
        
        //find # of open seats in the course
        String courseCode = (String) courseScheduleComboBox.getSelectedItem();
        int openSeats = openSeats(currentSemester, courseCode);

        //schedule or waitlist based on seat availability
        if(openSeats > 0){
            ScheduleEntry entry = new ScheduleEntry(currentSemester, courseCode, studentID, "S", Timestamp.from(Instant.now()));
            ScheduleQueries.addScheduleEntry(entry);
            String result = String.format("%s has been enrolled in %s.", studentName, courseCode);
            scheduleCourseResult.setText(result);
        }
        else{
            ScheduleEntry entry = new ScheduleEntry(currentSemester, courseCode, studentID, "W", Timestamp.from(Instant.now()));
            ScheduleQueries.addScheduleEntry(entry);
            String result = String.format("%s has been waitlisted for %s.", studentName, courseCode);
            scheduleCourseResult.setText(result);
        }
        
        //add course to combobox on drop course page 
        studentDropCourseComboBox.addItem(courseCode);
    }//GEN-LAST:event_scheduleCourseButtonActionPerformed

    private int openSeats(String semester, String courseCode){
        int totalSeats = CourseQueries.getCourseSeats(semester, courseCode);
        int filledSeats = ScheduleQueries.getScheduledStudentCount(semester, courseCode);
        System.out.println("total seats: "+totalSeats);
        System.out.println("filled seats: "+filledSeats);
        int openSeats = totalSeats - filledSeats;
        return openSeats;
    }
    
