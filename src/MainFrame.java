    private void updateStudentListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateStudentListButtonActionPerformed
        String courseCode = (String) courseListComboBox.getSelectedItem();
        ArrayList<ScheduleEntry> scheduledStudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode);
        ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);
        
        String[][] scheduledTableData = new String[scheduledStudents.size()][3];
        for(int i = 0; i < scheduledStudents.size(); i++){
            String studentID = scheduledStudents.get(i).getStudentID();
            StudentEntry student = StudentQueries.getStudent(studentID);
            
            scheduledTableData[i][0] = student.getLastName();
            scheduledTableData[i][1] = student.getFirstName();
            scheduledTableData[i][2] = studentID;
        }
        
        String[][] waitlistedTableData = new String[waitlistedStudents.size()][3];
        for(int i = 0; i < waitlistedStudents.size(); i++){
            String studentID = waitlistedStudents.get(i).getStudentID();
            StudentEntry student = StudentQueries.getStudent(studentID);
            
            waitlistedTableData[i][0] = student.getLastName();
            waitlistedTableData[i][1] = student.getFirstName();
            waitlistedTableData[i][2] = studentID;
        }
        
        scheduledStudentsTable.setModel(new javax.swing.table.DefaultTableModel(scheduledTableData, new String[]{"Last Name", "First Name", "ID"}));
        waitlistedStudentsTable.setModel(new javax.swing.table.DefaultTableModel(waitlistedTableData, new String[]{"Last Name", "First Name", "ID"}));

    }//GEN-LAST:event_updateStudentListButtonActionPerformed

    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        String studentName = (String) dropStudentComboBox.getSelectedItem();
        String studentID = getStudentID(studentName);
        
        //drop and remove from comboboxes
        StudentQueries.dropStudent(studentID);
        removeFromStudentLists(studentName);
        String action = String.format("%s has been dropped from the list of students\n", studentName);
        dropStudentTextArea.append(action);
        
        //for each semester, do the following:
        ArrayList<String> semesterList = SemesterQueries.getSemesterList();
        for(String semester : semesterList){
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            dropStudentTextArea.append(semester+"\n");
            
            for(ScheduleEntry entry : schedule){
                //drop course that the student was in
                String courseCode = entry.getCourseCode();
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
                
                //text area update
                action = String.format("%s has been dropped from %s.\n", studentName, courseCode);
                dropStudentTextArea.append(action);
            
                //find student with highest waitlist priority
                if(!ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode).isEmpty()){
                    ScheduleEntry waitlistEntry = (ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode)).get(0);
                        
                    //update waitlist student to be enrolled
                    ScheduleQueries.updateScheduleEntry(semester, waitlistEntry);
                        
                    //get waitlisted student's name
                    String waitlistID = waitlistEntry.getStudentID();
                    String waitlistName = getStudentName(waitlistID);
                    
                    //text area update
                    action = String.format("%s has been scheduled into %s.\n", waitlistName, courseCode);
                    dropStudentTextArea.append(action);
                }
            }
        }    
    }//GEN-LAST:event_dropStudentButtonActionPerformed

