    public void rebuildCourseLists(){
        courseScheduleComboBox.removeAllItems();
        courseListComboBox.removeAllItems();
        dropCourseComboBox.removeAllItems();
        studentDropCourseComboBox.removeAllItems();
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        
        for(CourseEntry course : courses){
            String courseCode = course.getCourseCode();
            
            //populate comboboxes
            courseScheduleComboBox.addItem(courseCode);
            courseListComboBox.addItem(courseCode);
            dropCourseComboBox.addItem(courseCode);
        }
    }
    
    public void rebuildStudentLists(){
        currentStudentComboBox.removeAllItems();
        dropStudentComboBox.removeAllItems();
        
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        
        for(StudentEntry student : students){
            String studentName = student.getLastName()+", "+student.getFirstName();
            
            //populate comboboxes
            currentStudentComboBox.addItem(studentName);
            dropStudentComboBox.addItem(studentName);
        }
    }
    
