    private void studentDropCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentDropCourseActionPerformed
        String studentID = currentStudent.getStudentID();
        String studentName = getStudentName(studentID);
        String courseCode = (String) studentDropCourseComboBox.getSelectedItem();
        
        //drop the course
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, studentID, courseCode);
        //remove course from combobox
        studentDropCourseComboBox.removeItem(courseCode);
        
        String action = String.format("%s has been dropped from %s.\n", studentName, courseCode);
        studentDropCourseTextArea.append(action);
        
        //schedule student from waitlist
        if(!ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode).isEmpty()){
            ScheduleEntry waitlistEntry = (ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode)).get(0);
                        
            //update waitlist student to be enrolled
            ScheduleQueries.updateScheduleEntry(currentSemester, waitlistEntry);
                        
            //get waitlisted student's name
            String waitlistID = waitlistEntry.getStudentID();
            String waitlistName = getStudentName(waitlistID);
                    
            //text area update
            action = String.format("%s has been scheduled into %s.\n", waitlistName, courseCode);
            studentDropCourseTextArea.append(action);
        }
    }//GEN-LAST:event_studentDropCourseActionPerformed

    private void rebuildstudentDropCourseComboBox(){
        studentDropCourseComboBox.removeAllItems();
        ArrayList<ScheduleEntry> entries = ScheduleQueries.getScheduleByStudent(currentSemester, currentStudent.getStudentID());
        for (ScheduleEntry entry : entries){
            String courseCode = entry.getCourseCode();
            studentDropCourseComboBox.addItem(courseCode);
        }
    }
    
